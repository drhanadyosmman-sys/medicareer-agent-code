export function t(key: string, lang: Language): string {
  const keys = key.split('.');
  let value: any = translations;
  for (const k of keys) {
    value = value?.[k];
  }
  if (value && typeof value === 'object' && 'en' in value) {
    return value[lang] || value.en || key;
  }
  return key;
}
